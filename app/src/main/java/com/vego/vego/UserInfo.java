package com.vego.vego;

public class UserInfo {
    String name;
    String weight;
    String height;
    String age;
    String userActivity;



    public UserInfo(String name, String weight, String height, String age, String userActivity) {
        this.name = name;
        this.weight = weight;
        this.height = height;
        this.age = age;
        this.userActivity = userActivity;
    }
}
